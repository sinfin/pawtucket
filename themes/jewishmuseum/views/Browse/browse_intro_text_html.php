	<h1>Jak hledat v&nbsp;katalogu sbírek</h1>

	<h2><b>Vyhledávání v&nbsp;plném textu</b></h2>
	<ul>
	<li>V poli <quot>Vyhledat ve sbírkách</quot> je možné vyhledávat na jakoukoli kombinaci slov či jejich částí ve všech relevantních polích digitálního katalogu. Na konci slov je možné použít * jako zástupný znak (například hledání na "terezín*" vyhledá "Terezín", "terezínské", atd.).</li>
	<li>Následně je možné výsledek hledání zpřesňovat na základě klíčových slov, datace, lidí, organizací, míst a&nbsp;dalších parametrů.</li>
	</ul>

	<br />
	<h2><b>Facetové vyhledávání</b></h2>
	<div>
	Sbírky je možné prohledávat také na základě strukturovaných vztahů k&nbsp;různým typům metadat (typy objektů, datace, lidé, organizace, místa, klíčová slova, sbírky). Toto "facetové" vyhledávání umožňuje postupně zužovat výsledek hledání.
	Jednotlivé facety se zobrazí po kliknutí na "Prohlížet sbírky" nebo po zadání hledání v&nbsp;plném textu (pokud odpovídají výsledku hledání).
	</div>

