  <p>The Drexel Historic Costume Collection was formed to serve as an  educational resource for the students of Drexel University. Through the  remarkable generosity of our donors, the collection has grown to be one of the  finest teaching collections in the United States. It is the mission of the DHCC  to honor the educational intent of these donors, while preserving the  collection for future generations. <br />
    This is to be  achieved by:</p>
  <ul type="disc">
    <li>Increasing exhibitions,       publications and lectures </li>
    <li>Providing access to fragile objects       in the DHCC through the online collections database </li>
    <li>Taking a leadership role in       research, scholarship and thoughtful integration of garments into the       educational programs of Drexel University </li>
    <li>Augmenting the DHCC through       judicious acquisitions </li>
    <li>Conserving, cataloguing, and       photographing existing garments </li>
  </ul>
  <p>If you would like to make contribution to the Collection or help  to fund our educational and conservation activities, please contact our  curator, Clare Sauro, <a href="mailto:cms393@drexel.edu">cms393@drexel.edu</a>, 215-571-3504.</p>
