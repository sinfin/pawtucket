		<b>Idee und Realisierung</b><br/><br/>
Seit Ende der 1970er Jahre wurden in der Sparkasse Bad Hersfeld 
verstärkt ausrangierte Geräte gesammelt. Die Idee, diese Objekte in 
einem Museum zu präsentieren konkretisierte sich im Jahre 2003: Auf 
Initiative des Vorstandsvorsitzenden Dr. Manfred Wagner entstand im 
Zuge der Dachsanierung der heutige Museumsraum. Für die Einrichtung 
und Betreuung des Museums sollte ein Team ehemaliger Mitarbeiter 
sorgen. Die Kooperation mit qualifizierten Museumsspezialisten sorgte 
dafür, dass die Ausstellung in wissenschaftlicher, didaktischer und 
gestalterischer Hinsicht hohen Ansprüchen genügt. <br/><br/>
 
Im Zuge der Museumskonzeption wurden zahlreiche alte Akten, Geschäftsbücher,
Zeitschriftensammlungen, Fotos, Geräte und Ähnliches 
zusammengetragen. Für die Ausstellung konnte davon nur ein kleiner 
Teil eingesetzt werden und einige Themen blieben unerwähnt. So ent- 
stand die Idee, nach und nach Teile des Archivbestandes zu digitalisieren
und Interessierten zugänglich zu machen. Gefördert vom Vorstandsvorsitzenden
Reinhard Faulstich, wurde auch dieses Projekt durch 
die Zusammenarbeit zwischen dem ehrenamtlichen Museumsteam und 
Profis aus den Bereichen Wissenschaft und Gestaltung möglich.

