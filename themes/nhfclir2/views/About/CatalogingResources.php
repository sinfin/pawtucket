	<a name="PBCore"></a><H2>PBCore</H2>
	<div class="textContent">
		<div>
			<b>PBCore</b> is a metadata standard for audiovisual media, created for the description of analog and digital objects to support exchange of information. PBCore development (launched in 2005) was funded by the Corporation for Public Broadcasting based on Dublin Core, for use with audiovisual media and with the input of community members in a range of institutions, including and well beyond broadcast.
		</div>
	</div><!--end textContent -->
