	<div class="textContent">
		<div>
			<b>Describing Archives: A Content Standard</b> supports exchange of information on archival collections, recognizing that materials created and gathered by a person, family or organization are intentionally and consciously assembled for some purpose (DACS viii.)  Providing information about the provenance, or context, of amateur film collections helps researchers exploring their cultural history.  
