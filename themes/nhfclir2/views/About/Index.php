	<H2>Film Preservation</H2>
	<div>
		Motion picture film is fragile, requiring proper care and storage conditions. The National Film Preservation Foundation, affiliated with the Library of Congress's National Film Preservation Board, shares information including <a href="http://www.filmpreservation.org/preservation-basics" target="_blank">Why Preserve Film?</a>
	</div>
	<div>
		<i>The Film Preservation Guide: The Basics for Archives, Libraries, and Museums</i> is available as a PDF <a href="http://www.filmpreservation.org/preservation-basics/the-film-preservation-guide">here</a>.  
	</div>
	<div>
		Cold storage and film-to film copying have been front line methods for film preservation. Digital preservation, which has cost and sustainability implications, is on the horizon. Read information from the <a href="http://www.archives.gov/preservation/products/definitions/mopix-opt.html" target="_blank">National Archives</a> regarding digital options for preservation, reproduction, and distribution. <a href="http://amianet.org/index.php" target="_blank">The Association of Moving Image Archivists</a> organizes an annual conference and has a listserv with active preservation discussions.     
	</div>
<div class="divide"><!-- empty --></div><!-- end divide -->
	<H2>Archival Materials</H2>
	<div>
		"Libraries, archives, and cultural institutions hold millions of items that have never been adequately described."  <i><a href="http://www.clir.org/hiddencollections/" target="_blank">CLIR Cataloging Hidden Special Collections and Archives</a></i>
	</div>
	<div>
		The three institutions in this project have education and access missions and are custodians of important collections. The public has not been aware of many of these.  Amateur film, in particular, may not have been described or copied for access. Because curators, historians and media scholars are now more interested in amateur film, it is becoming more of a priority.
	</div>
	<div>
		It is important that organizations with an audiovisual preservation mission learn, use, and make known best practices for preservation and access in a time of transition from analog to digital access.  The point of our collaboration is to use standards-based description to expose films so as to maintain custodial and research access to information on the physical material and its provenance. This is how we will help ensure preservation and a greater understanding of personal films and their role in communications and culture.
	</div>
