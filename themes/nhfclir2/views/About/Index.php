	<H2>Amateur Film</H2>
	<div>
		The premise of the project is that a reel of amateur film is a historical document. The video surrogates of the amateur film reels streaming on this site present the reels as they were created, with no added edits. The original films, almost all of which are reversal (the film was exposed, processed and then used for projection), are in the custody of three cultural organizations--two museums and a moving image archives. 
	</div>
	<div>
		For more information on the physical properties of amateur film we recommend <a href="http://www.littlefilm.org" target="_blank">LittleFilm.org</a> and <a href="http://www.filmforever.org" target="_blank">Filmforever.org</a>.
	</div>
	<div>
		"The importance of amateur and nontheatrical filmmaking within the discipline of film history is just beginning to be investigated....1938-1940 has long been understood as a time of intense productivity and significance in Hollywood filmmaking, but Hollywood is only a portion of the full picture of film history during the late 1930s. The amateur films from this period can provide a richer understanding of how Americans saw themselves through their own filmmaking practices."  <i>Oliver Gaycken, Ph.D., co-founder of the Non-Theatrical Film and Media Special Interest Group, Society for Cinema and Media Studies.</i>  
  	</div>
