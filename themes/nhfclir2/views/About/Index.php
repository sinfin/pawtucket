	<H2>New York World's Fair, 1939-1940</H2>
	<div>
		The New York World's Fair opened on April 30, 1939, in Queens, NY.  In the first year it closed at the end of October. The second year ran from May through October 1940. It was in Flushing Meadows, NY, on reclaimed land, which is presently <a href="http://www.nycgovparks.org/sub_your_park/vt_flushing_meadows/vt_flushing_meadows_park.html" target="_blank">Flushing Meadows Corona Park</a>.   
	</div>
	<div>
		Government and corporate exhibitions, as well as many entertainments and services attracted visitors to the Fair. The theme was "Building the World of Tomorrow." With the onset of World War II some countries were absent, while some zones and exhibitors changed. The Fair's theme became "Peace and Freedom."  
	</div>
