<div style="font-weight:bold; padding-bottom:0px;">Contribute to the Collection</div><br/>

Have you saved materials that speak to your experiences during the terrorist attacks of September 11, 2001 and February 26, 1993 and their aftermath?

The Museum is actively acquiring materials for its permanent collection such as photographs, videotapes, voice messages, recovered property, clothing and other personal effects, workplace memorabilia, incident-specific documents, and original writings including letters, e-mails and diaries that help to illuminate people’s experiences during and after September 11, 2001 and February 26, 1993. 

If you would like to learn more about donating material related to a friend or loved one who was killed as a result of the attacks, please click here.

If you would like more information about donating to the collection, please contact Museum Collections at 212-312-8800 or collections@911memorial.org.
