<div style="font-weight:bold; padding-bottom:0px;"><b>Building the Permanent Collection</b></div><br/>

The Museum’s growing collection of diverse materials includes artifacts, photographs, audio and video tapes, personal effects and memorabilia, expressions of tribute and remembrance, recorded testimonies and digital files and websites related to the history of the World Trade Center, the events of September 11, 2001 and February 26, 1993, and the repercussions of these seismic attacks. We invite you to explore selections from the collection.
